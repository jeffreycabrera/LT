						Absence without Official Leave (AWOL)<br>
						<i>Note:</i>  For a worker not to be considered AWOL the following procedures must be strictly followed:<br><br>

						1.	He must secure prior permission from his immediate superior at least one (1) day in advance and accomplish an Application for Leave of Absence Form for approval.  It should be noted that Application for Leave of Absence is only a request and may be disapproved in favor of the operational requirements of the Company.  This should be charged to VL credits or deducted from the employee’s salary, as applicable.<br><br>

						2.	In case of emergency, illness, accidents and other compelling reasons, a worker must inform his immediate superior or department head within the day of his absence about his inability to report for work by telephone, e-mail, or text message with confirmation of receipt within the first hour of the start of his work schedule.  Upon his return to work, he must explain the cause of his absence and fill out the appropriate Leave Application Form.  This should be charged to VL or SL credits (whichever is applicable) or deducted from the employee’s salary, as applicable.<br><br>
						
						3.	A worker shall be entitled to sick leave with pay only if he is actually sick.  Hence, if a worker calls in sick but is proven to be otherwise, the worker will be considered malingering and will be dealt with corresponding disciplinary action when necessary.<br><br>
						
						4.	A worker who has been sick for more than two (2) days will be required to present a medical certificate from his doctor and attach the said certificate to his Leave Application Form for approval by his immediate superior and forward to HRD for proper recording.  The Company may require home visits to verify reports of inability of employees to report for work on account of sickness.<br><br>
						
						5.	Any unauthorized absence and/or approved leaves but without Leave Application Form submitted to HR during payroll cut-off shall be considered as absence without leave (AWOL) and the employee shall not receive any pay for the day/s that he has been on AWOL nor his absence be allowed to be charged against his earned leave credits.<br><br>
						
						6.	Absence without official leave constituting at least three (3) consecutive working days shall be considered as abandonment of work and is ground for dismissal.<br><br>
						
						7.	Absence before and after the holiday must be pre-approved by the immediate superior or department head thru an Application for Leave of Absence.  Otherwise, an employee’s absence may be considered AWOL. 
