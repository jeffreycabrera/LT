				Q: 	What happens if my manager does not approve or disapprove my leave in the Leave Tracker?
				<br>A: 	You are responsible in reminding your manager of your upcoming leave; just as your manager is responsible for responding to leave requests. In the Leave Tracker, the status will be set as "Follow-up"<br><br>

				<br>Q:	What happens if I do not have any leave available?
				<br>A:	Leave days are earned through work, as much as possible, do not go on leave. If it is necessary, you should inform your manager and file for leave without pay (LWP)<br><br>

				<br>Q:	What happens if I do not have enough leave available?
				<br>A:	Leave days are earned through work, as much as possible, do not go on leave. If it is necessary, you should inform your manager and file for leave without pay (LWP)<br><br>

				<br>Q:	What happens to my unused leave at the end of the year?
				<br>A:	Up to 10 PTO days are cashable, the rest will be forfeited <br><br>

				<br>Q:	Is carrying over of leave to the next year allowed?
				<br>A:	Only for those that were requested and approved prior to the new year for the month of January<br><br>

				<br>Q: 	What happens if my leave was not approved but I still went on leave?
				<br>A:	This would be considered a violation, and set as AWOL (absence without leave), a permanent mark will go against your employee file. It is also a grounds for termination<br><br>
